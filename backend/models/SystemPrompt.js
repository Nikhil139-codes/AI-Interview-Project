const mongoose = require('mongoose');

const systemPromptSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
    },
    content: {
      type: String,
      required: true,
    },
    description: {
      type: String,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  }
);

const SystemPrompt = mongoose.model('SystemPrompt', systemPromptSchema);

module.exports = SystemPrompt;
